function bulbOn(){
    let bulbobj=document.querySelector(".bulboff")
    bulbobj.setAttribute("src","https://img.freepik.com/free-vector/realistic-light-bulb-with-electricity_23-2149129410.jpg")
}

function bulbOff(){
    let bulbobj=document.querySelector(".bulboff")
    bulbobj.setAttribute("src","https://toppng.com/uploads/preview/light-bulb-on-off-png-11553940208oq66nq8jew.png")
}